// Inicializar el directorio de capturas
function initScreenshotDir() {
  const screenshotDir = path.join(__dirname, 'screenshots');
  if (!fs.existsSync(screenshotDir)) {
    fs.mkdirSync(screenshotDir);
  }
  return screenshotDir;
}

